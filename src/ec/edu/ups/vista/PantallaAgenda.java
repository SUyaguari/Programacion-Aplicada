    private void jTblTelefonosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTblTelefonosMouseClicked
        int pos = jTblTelefonos.getSelectedRow();
        
        int id = (Integer) jTblTelefonos.getValueAt(pos, 0);
        String numero = (String) jTblTelefonos.getValueAt(pos, 1);
        String tipo = (String) jTblTelefonos.getValueAt(pos, 2);
        String operadora = (String) jTblTelefonos.getValueAt(pos, 3);
        
        telefono = new Telefono(id, numero, tipo, operadora, persona);
        
        habilitarBotonesEditar();
        deshabilitarParcialBotones();
    }//GEN-LAST:event_jTblTelefonosMouseClicked

