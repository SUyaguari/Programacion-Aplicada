    public void limpiarTabla(){
        DefaultTableModel modelo = (DefaultTableModel) jTblTelefonos.getModel();
        modelo.setRowCount(0);
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
        }
    }

    public void llenarTabla(){
        DefaultTableModel modelo = (DefaultTableModel) jTblTelefonos.getModel();
        modelo.setRowCount(0);
        int i = Integer.parseInt(jTextFieldId.getText());
        
        List<Telefono> listaTelefono = controladorTelefono.buscarTelefonoPersona(i);
        
        
        if(listaTelefono.isEmpty()){
            JOptionPane.showMessageDialog(this, "No hay telefonos registrados a este usuario");
        }else{
            for (Telefono telefono : listaTelefono) {
                Object[] objeto = {telefono.getCodigo(), telefono.getNumero(), telefono.getTipo(), telefono.getOperadora()};
                modelo.addRow(objeto);
            }
        }
    }
    
