    private void jBtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnBuscarActionPerformed
        String cedula = jTextBuscarUsuario.getText();
        
        Persona persona = controladorPersona.read(p -> cedula.equals(p.getCedula()));
        
        if(persona!=null){
            jTextFieldId.setText(persona.getId()+"");
            jTextCedula.setText(persona.getCedula());
            jTextNombre.setText(persona.getNombre());
            jTextApellido.setText(persona.getApellido());
            jTextDireccion.setText(persona.getDireccion());
            this.persona = persona;
            limpiarTabla();
            llenarTabla();
            activarParcialBotones();
        }else{
            JOptionPane.showMessageDialog(this, "Cedula no encontrada");
        }
    }//GEN-LAST:event_jBtnBuscarActionPerformed

