    private void btnAgregarPar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarPar1ActionPerformed
        String i = JOptionPane.showInputDialog(this, "Ingrese el numero de cedula");
        Persona persona = controladorPersona.buscarCedula(i);
        if(persona!=null){
            txtCedulaPar1.setText(persona.getCedula());
            lbPersonaPar1.setVisible(true);
            listaParImpar.put(1, new ControladorParImpar(persona, txtNumeroPar1, txtAreaPar));
        }else{
            JOptionPane.showMessageDialog(this, "No se ha encontrado al jugador con ese numero de cedula");
        }
    }//GEN-LAST:event_btnAgregarPar1ActionPerformed

