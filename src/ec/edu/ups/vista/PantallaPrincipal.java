    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
       
        /*PersonaBD pe = new PersonaBD();
        pe.setApellido("admin");
        pe.setCedula("admin");
        pe.setContraseña("admin");
        pe.setCorreo("admin");
        pe.setNombre("admin");
        pe.setRol("Administrador");
        controladorPersona.create(pe);
        */
        String correo = txtusuario.getText();
        String contraseña = txtContraseña.getText();

        if(correo.isEmpty() || contraseña.isEmpty()){
            JOptionPane.showMessageDialog(null, "Llene todos los campos");
        }else{
            PersonaBD p = controladorPersona.iniciarSesion(correo, contraseña);
            if(p!=null && (p.getRol().equals("Administrador") || p.getRol().trim().equals("Usuario"))){
                pantallaVehiculos = new PantallaVehiculos(p);
                pantallaVehiculos.setVisible(true);
                this.dispose();
            }
        }
    }//GEN-LAST:event_btnEntrarActionPerformed
   
