    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        
        int i;
        int izquierda;
        int derecha;

        
        for (i = 0; i < creacion; i++) {
            
            izquierda = i - 1;
            
            if (izquierda < 0) {

                
                izquierda = creacion-1;
                
            }

            
            derecha = i;

            comensales = new Filosofos(i, botones[izquierda], tenedor[derecha], tenedor[izquierda], resultado[i], txtInformacion);
            
        }
    }//GEN-LAST:event_btnIniciarActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        removerComponentes();
        creacion++;
        botones = new JButton[creacion];
        tenedor = new JButton[creacion];
        resultado = new JTextField[creacion];
        label = new JLabel[creacion];
        crearFilosofos();
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnReiniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReiniciarActionPerformed
        removerComponentes();
        creacion=5;
        crearFilosofos();
    }//GEN-LAST:event_btnReiniciarActionPerformed

