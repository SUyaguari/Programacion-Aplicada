    private void jBtnComenzarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnComenzarActionPerformed
        String usuario = jtxtUsuario.getText();
        String contraseña = jtxtContraseña.getText();
        if(usuario.isEmpty() || contraseña.isEmpty()){
            JOptionPane.showMessageDialog(this, "Llene los campos");
        }else{
            Usuario u = (Usuario) controladorLogin.validarLogin(new Usuario(usuario, contraseña));
            if(u!=null){
                pantallaAgenda.setVisible(true);
                this.hide();
            }else{
                JOptionPane.showMessageDialog(this, "Usuario y/o contraseña erroneos");
            }
        }
    }//GEN-LAST:event_jBtnComenzarActionPerformed

