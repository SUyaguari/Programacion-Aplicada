    public void limpiar(){
         DefaultTableModel modelo = (DefaultTableModel) tblRegistros.getModel();
        modelo.setRowCount(0);
        
        for (int i = 0; i < tblRegistros.getRowCount(); i++) {
            modelo.removeRow(i);
        }
        tblRegistros.setModel(modelo);
    }
     public void listar(List<Registro> lista ){
        
        DefaultTableModel modelo = (DefaultTableModel) tblRegistros.getModel();
        if(lista.isEmpty()){
                JOptionPane.showMessageDialog(this, "No existe ningun cliente registrado");
        }else{
            for (Registro registro : lista) {
                Object[] o = {registro.getId(), registro.getNumero(), registro.getValor(), 
                    registro.getTipo(), registro.getModalidad()};
                modelo.addRow(o);
            }
        }
        
        tblRegistros.setModel(modelo);
    }

