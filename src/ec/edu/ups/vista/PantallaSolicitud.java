    private void btnCedulaClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCedulaClienteActionPerformed
        String cedula = txtCedulaCliente.getText().trim();
        System.out.println(cedula);
        
        if(cedula.isEmpty()){
            JOptionPane.showMessageDialog(this, "Ingrese una cedula");
        }else{
            Persona persona=controladorPersona.buscarCedula(cedula);
            if(persona!=null){
                txtNombreCLiente.setText(persona.getNombres());
                txtApellidoCLiente.setText(persona.getApellidos());
                SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
                String fecha = formato.format(persona.getFechaNacimiento());
                txtFechaCliente.setText(fecha);
                txtSueldoCLiente.setText(persona.getSueldo()+"");
            }else{
                JOptionPane.showMessageDialog(this, "Cliente no encontrado");
            }
        }
    }//GEN-LAST:event_btnCedulaClienteActionPerformed

